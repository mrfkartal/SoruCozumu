public class forloop1 {

    /*
    1'den 5'e kadar olan sayıların karelerini ekrana yazdır.
     */

    public static void main(String[] args) {
        for (int i = 1; i <=5 ; i++) {
            System.out.println("karekok icinde"+i*i+ "=" +i+ "demektir" );
        }
    }


}
